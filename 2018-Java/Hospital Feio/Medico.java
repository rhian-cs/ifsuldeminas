public class Medico 
{
	private String	nome;
	private int		idade;
	private String	sexo;
	private double	salario;
	private int		codCRM;
	
	//Construtor
	Medico(String nome, int idade, String sexo, double salario, int codCRM)
	{
		this.nome		=	nome;
		this.idade		=	idade;
		this.sexo		=	sexo;
		this.salario	=	salario;
		this.codCRM		=	codCRM;
	}
}
