public class Enfermeiro 
{
	private String	nome;
	private int		idade;
	private String	sexo;
	private double	salario;
	private int		codCOREN;
	
	//Construtor
	Enfermeiro(String nome, int idade, String sexo, double salario, int codCOREN)
	{
		this.nome			=	nome;
		this.idade			=	idade;
		this.sexo			=	sexo;
		this.salario		=	salario;
		this.codCOREN		=	codCOREN;
	}
}
