    private void botaoCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCadastrarActionPerformed
        //Pega os dados
        String resultado = null;
        
        String nome = jTextFieldNome.getText();
        String email = jTextFieldEmail.getText();
        String cpf = jTextFieldCpf.getText();
        String cep = jTextFieldCep.getText();
        String bairro = jTextFieldBairro.getText();
        String rua = jTextFieldRua.getText();
        String numero = jTextFieldNumero.getText();
        String cidade = jTextFieldCidade.getText();
        String nacionalidade = jTextFieldNacionalidade.getText();
        String senha = new String(jTextFieldSenha.getPassword());
        String confirmarSenha = new String(jTextFieldConfirmarSenha.getPassword());
        try {
            //Tira os espaços
            resultado = new TelaCadastroAtendenteController().acaoBotaoCadastrar(
                nome.trim(),
                email.trim(),
                cpf.trim(),
                cep.trim(),
                bairro.trim(),
                rua.trim(),
                numero.trim(),
                cidade.trim(),
                nacionalidade.trim(),
                senha.trim(),
                confirmarSenha.trim()
            );
        } catch (Exception ex) {
            Logger.getLogger(TelaCadastroAtendenteView.class.getName()).log(Level.SEVERE, null, ex);
        }
        String mensagemSucesso = "Cadastro efetuado com sucesso. Seu id de login é ";
        if (resultado.length() >= mensagemSucesso.length()) {
                if (resultado.substring(0, mensagemSucesso.length()).equals(mensagemSucesso)) {
                        JOptionPane.showMessageDialog(null, resultado, "Sucesso ao cadastrar", JOptionPane.INFORMATION_MESSAGE);
                        botaoCancelar.doClick();
                } else {
                        JOptionPane.showMessageDialog(null, resultado, "Erro ao cadastrar", JOptionPane.WARNING_MESSAGE);
                }
        }
        else {
                JOptionPane.showMessageDialog(null, resultado, "Erro ao cadastrar", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_botaoCadastrarActionPerformed

