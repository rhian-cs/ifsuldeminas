    private void jTextFieldCepMouseExited(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTextFieldCepMouseExited
        buscaCEP b = new buscaCEP();
        String cep = jTextFieldCep.getText();
        try {
            String rua = b.getEndereco(cep);
            jTextFieldRua.setText(rua);
            String bairro = b.getBairro(cep);
            jTextFieldBairro.setText(bairro);
            String cidade = b.getCidade(cep);
            jTextFieldCidade.setText(cidade);
        } catch (IOException ex) {
            Logger.getLogger(TelaCadastroAtendenteView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jTextFieldCepMouseExited

